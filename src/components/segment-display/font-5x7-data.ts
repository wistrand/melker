let _decoded: Uint8Array | null = null;

/** Get the raw PSF2 bytes, inflating from compressed base64 on first call. */
export async function getPSF2Bytes(): Promise<Uint8Array> {
  if (_decoded) return _decoded;
  const bin = atob(PSF2_DEFLATE_B64);
  const compressed = new Uint8Array(bin.length);
  for (let i = 0; i < bin.length; i++) compressed[i] = bin.charCodeAt(i);
  const ds = new DecompressionStream("deflate");
  const writer = ds.writable.getWriter();
  writer.write(compressed);
  writer.close();
  const chunks: Uint8Array[] = [];
  const reader = ds.readable.getReader();
  while (true) {
    const { done, value } = await reader.read();
    if (done) break;
    chunks.push(value);
  }
  const totalLen = chunks.reduce((s, c) => s + c.length, 0);
  _decoded = new Uint8Array(totalLen);
  let off = 0;
  for (const c of chunks) { _decoded.set(c, off); off += c.length; }
  return _decoded;
}

